#include <iostream>
using namespace std;



int main()
{

    //for loop (initializing+start;end condition;increment/change)
    for(int i = 1; i <= 5; i++)
    {
        cout << i <<endl;
    }

    int nums[] = {1, 2, 5, 7, 3};
    for(int x = 0; x < 5; x++)
    {
        cout << nums[x] << endl;
    }

    return 0;
}
